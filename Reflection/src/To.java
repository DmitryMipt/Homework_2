public class To {
    private Number a;
    private String b;
    private boolean k;

    public To() {
    }

    public To(int a, String b, boolean k) {
        this.a = a;
        this.b = b;
        this.k = k;
    }

    public Number getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public boolean isK() {
        return k;
    }

    public void setK(boolean k) {
        this.k = k;
    }
}
