public class From {
    private int a;
    private String b;
    private float f;
    private boolean k;

    public From() {
    }

    public From(int a, String b, float f, boolean k) {
        this.a = a;
        this.b = b;
        this.f = f;
        this.k = k;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }

    public float getF() {
        return f;
    }

    public void setF(float f) {
        this.f = f;
    }

    public boolean isK() {
        return k;
    }

    public void setK(boolean k) {
        this.k = k;
    }
}
